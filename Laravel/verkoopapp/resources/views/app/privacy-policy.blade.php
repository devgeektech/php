be able to take full advantage of our Services.</li>
<li>Pixels. We may from time to time use certain Facebook
Business Tools such as the Facebook Pixel. This tool
allows Verkoop to keep track of what users do after they see or
click on a Facebook advertisement and enables us to monitor
the effectiveness of Facebook ads for purposes of statistics
and market research. Verkoop will be able to better understand
and deliver ads and make them more relevant to you. The
collected data remains anonymous and Verkoop cannot see
the personal data of any individual user. However, the
collected data is saved and processed by Facebook.
Facebook may be able to connect the data with your Facebook
account and use the data for their own advertising purposes
and in accordance with Facebook’s Data Use Policy. Facebook
has ultimate control of the information gathered through the
Facebook Business Tools and communications. You can optout of Facebook’s use of cookies and Facebook Pixel through
settings on your Facebook Account or withdraw your consent.</li>
<li>Clear gifs. We employ a software technology called clear gifs
(a.k.a. web beacons or web bugs) to help us better manage
content on our Services by informing us what content is
effective. Clear gifs are tiny graphics with a unique identifier similar in function to cookies, and are used to track the online
movements of web users. In contrast to cookies, which are
stored on a user's computer hard drive, clear gifs are
embedded invisibly on web pages and are about the size of
the period at the end of this sentence. Data collected in this
way is anonymous to us, which means we cannot see the
personal data of individual users. However, this data is saved
and processed by Facebook. Facebook can connect this data
with your Facebook account and use it for its own advertising
purposes. You can allow Facebook and its partners to place
ads on and outside of Facebook. A cookie can also be saved
on your device for these purposes.
</li>
<li>Flash cookies. We use Flash cookies (a.k.a local shared
objects or LSOs) to store some of your viewing preferences on
our Services. These are used to collect and store information,
but differ from browser cookies in the amount, type and
manner in which data is stored.</li>
</ul>
